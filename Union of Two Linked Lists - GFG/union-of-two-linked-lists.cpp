class Solution
{
    public:
    struct Node* makeUnion(struct Node* h1, struct Node* h2)
    {
        set<int> s;
        Node *list = new Node(0), *h = list;
        
        while(h1) { s.insert(h1->data); h1 = h1->next; }
        while(h2) { s.insert(h2->data); h2 = h2->next; }
        for(auto i : s) {
            list->next = new Node(i);
            list = list->next;
        }
        
        return h->next;
    }
};


